translations['ar-ma'] = { ...translations.ar };

const localeDir = path.join(__dirname, '..', 'src', 'i18n', 'locales');
const en = require(path.join(localeDir, 'en.json'));

// Get list of untranslated keys
const untranslatedKeys = Object.keys(en.home).filter(k => {
  const enVal = en.home[k];
  // Check ar.json as reference - if same as EN, it's untranslated
  const ar = require(path.join(localeDir, 'ar.json'));
  return typeof enVal === 'string' && ar.home && ar.home[k] === enVal;
});

console.log(`Found ${untranslatedKeys.length} untranslated keys to fix`);

let totalUpdated = 0;

for (const [locale, trans] of Object.entries(translations)) {
  const filePath = path.join(localeDir, `${locale}.json`);
  if (!fs.existsSync(filePath)) {
    console.log(`SKIP: ${locale}.json not found`);
    continue;
  }

  const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));

  if (!data.home) data.home = {};

  let updated = 0;
  for (const key of untranslatedKeys) {
    if (trans[key]) {
      data.home[key] = trans[key];
      updated++;
    }
  }

  if (updated > 0) {
    fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8');
    console.log(`${locale}: updated ${updated} keys`);
    totalUpdated += updated;
  }
}

console.log(`\nTotal: ${totalUpdated} translations updated across ${Object.keys(translations).length} locales`);
